    @Test
    @Transactional
    void createProject() throws Exception {
        int databaseSizeBeforeCreate = projectRepository.findAll().size();
        // Create the Project
        ProjectDTO projectDTO = projectMapper.toDto(project);
        restProjectMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(projectDTO)))
            .andExpect(status().isCreated());

        // Validate the Project in the database
        List<Project> projectList = projectRepository.findAll();
        assertThat(projectList).hasSize(databaseSizeBeforeCreate + 1);
        Project testProject = projectList.get(projectList.size() - 1);
        assertThat(testProject.getName()).isEqualTo(DEFAULT_NAME);
        assertThat(testProject.getObjective()).isEqualTo(DEFAULT_OBJECTIVE);
        assertThat(testProject.getDelay()).isEqualTo(DEFAULT_DELAY);
        assertThat(testProject.getResources()).isEqualTo(DEFAULT_RESOURCES);
        assertThat(testProject.getVersion()).isEqualTo(DEFAULT_VERSION);
    }

